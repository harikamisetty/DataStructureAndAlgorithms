public class ListNode_I {
	public int val;
	public ListNode_I next;

	public ListNode_I(int x) {
		val = x;
	}

	public int getVal() {
		return val;
	}

	public void setVal(int val) {
		this.val = val;
	}

	public ListNode_I getNext() {
		return next;
	}

	public void setNext(ListNode_I next) {
		this.next = next;
	}
}
